import { useState, useEffect } from 'react';
import { collection, query, where, onSnapshot, addDoc, doc, updateDoc, deleteDoc, setDoc, serverTimestamp, getDocs, getDoc, db } from '../firebase';
import { v4 as uuidv4 } from 'uuid';
import FeedbackModal from './FeedbackModal';

const Matchmaker = ({ user, profile, onRoomJoined }) => {
  const [isSearching, setIsSearching] = useState(false);
  const [waitingUsers, setWaitingUsers] = useState([]);
  const [status, setStatus] = useState('idle'); // idle, searching, matched
  const [searchStartTime, setSearchStartTime] = useState(null);
  const [unsubscribe, setUnsubscribe] = useState(null);
  const [activeUsersCount, setActiveUsersCount] = useState(0);
  const [showFeedbackModal, setShowFeedbackModal] = useState(false);
  const [playMusic, setPlayMusic] = useState(() => localStorage.getItem("playMusic") !== "false");

  // Debug loggaus
  console.log("Matchmaker saanut props:", { 
    user: !!user, 
    profile: !!profile,
    profileAgeGroup: profile?.ageGroup
  });

  // Kuuntele odottavia k√§ytt√§ji√§ (KAIKKI, ei vain sama ik√§ryhm√§)
  useEffect(() => {
    const q = query(collection(db, 'waiting'));

    const unsubscribe = onSnapshot(q, (snapshot) => {
      const users = snapshot.docs
        .map(doc => ({
          id: doc.id,
          ...doc.data()
        }))
        .filter(waitingUser => waitingUser.uid !== user.uid); // Suodata oma uid pois
        
      setWaitingUsers(users);
      
      // ‚ùå POISTETTU AUTOMAATTINEN ROOM LUONTI
      // T√§m√§ rikkoi olemassa olevia pareja kun kolmas k√§ytt√§j√§ tuli mukaan
      // Nyt matchmaking tapahtuu vain startSearching funktiossa
    });

    return unsubscribe;
  }, [user.uid]); // Ei en√§√§ riippuvuutta ik√§ryhm√§st√§

  // Kuuntele aktiivisten k√§ytt√§jien m√§√§r√§√§
  useEffect(() => {
    const unsubscribeWaiting = onSnapshot(collection(db, 'waiting'), (snapshot) => {
      setActiveUsersCount(snapshot.size);
    });

    return unsubscribeWaiting;
  }, []);

  // Automaattinen status-reset jos jumittuu
  useEffect(() => {
    if (status !== 'idle' && status !== 'searching' && status !== 'matched') {
      console.log("üîÑ Automaattinen status-reset:", status, "-> idle");
      setStatus('idle');
      setIsSearching(false);
    }
  }, [status]);

    // Optimoitu siivous: v√§hemm√§n aggressiivinen ja tehokkaampi
  useEffect(() => {
    const optimizedCleanup = async () => {
      try {
        const now = Date.now();
        const TEN_MINUTES_AGO = now - (10 * 60 * 1000); // Kasvatettu 5min -> 10min
        
        console.log("üßπ Suoritetaan kevyt siivous...");
        
        // K√§yt√§ vain yhdistettyj√§ kyselyit√§ ja ehtojen optimointia
        const [waitingSnapshot, roomsSnapshot] = await Promise.all([
          getDocs(collection(db, 'waiting')),
          getDocs(collection(db, 'rooms'))
        ]);
        
        // Siivoa vain jos l√∂ytyy paljon vanhentuneita dokumentteja
        const staleWaiting = waitingSnapshot.docs.filter(doc => {
          const age = now - (doc.data().timestamp || 0);
          return age > TEN_MINUTES_AGO;
        });
        
        const staleRooms = roomsSnapshot.docs.filter(doc => {
          const data = doc.data();
          const age = now - (data.createdAt?.toDate?.()?.getTime() || 0);
          return age > TEN_MINUTES_AGO || data.isActive === false;
        });
        
        // Siivoa vain jos kannattaa (yli 3 dokumenttia)
        if (staleWaiting.length > 3) {
          console.log(`üóëÔ∏è Siivotaan ${staleWaiting.length} vanhaa waiting-k√§ytt√§j√§√§`);
          await Promise.all(staleWaiting.map(doc => deleteDoc(doc.ref)));
        }
        
        if (staleRooms.length > 3) {
          console.log(`üóëÔ∏è Siivotaan ${staleRooms.length} vanhaa huonetta`);
          await Promise.all(staleRooms.map(doc => deleteDoc(doc.ref)));
        }
        
      } catch (error) {
        console.error("‚ùå Virhe optimoidussa siivouksessa:", error);
      }
    };

    // Siivous vain kerran komponentin latauksen yhteydess√§
    optimizedCleanup();
    
    // Sitten harvemmmin - 5 minuutin v√§lein sen sijaan ett√§ 30s
    const interval = setInterval(optimizedCleanup, 5 * 60 * 1000);
    
    return () => clearInterval(interval);
  }, []);

  // Cleanup musiikki ja timeoutit kun komponentti poistuu
  useEffect(() => {
    return () => {
      // Pys√§yt√§ musiikki kun poistutaan Matchmaker-komponentista
      if (window.backgroundMusic) {
        window.backgroundMusic.pause();
        window.backgroundMusic.currentTime = 0;
        window.backgroundMusic = null;
      }
      
      // üîß MEMORY LEAK FIX: Siivoa fallback timeout
      if (window.matchmakerFallbackTimeout) {
        clearTimeout(window.matchmakerFallbackTimeout);
        window.matchmakerFallbackTimeout = null;
      }
    };
  }, []);

  // Kuuntele localStorage-muutoksia (musiikki-asetukset)
  useEffect(() => {
    const handleStorageChange = () => {
      const musicSetting = localStorage.getItem("playMusic") !== "false";
      setPlayMusic(musicSetting);
    };

    // Kuuntele storage-tapahtumia (toinen v√§lilehti muuttaa asetusta)
    window.addEventListener('storage', handleStorageChange);
    
    // Kuuntele my√∂s custom-tapahtumaa samalla v√§lilehdell√§
    window.addEventListener('musicSettingChanged', handleStorageChange);

    return () => {
      window.removeEventListener('storage', handleStorageChange);
      window.removeEventListener('musicSettingChanged', handleStorageChange);
    };
  }, []);

  // Aloita musiikki automaattisesti oletuksena
  useEffect(() => {
    if (playMusic) {
      // Varmista ettei musiikki jo soi
      if (!window.backgroundMusic) {
        const audio = new Audio('/meditation-relaxing-music-293922.mp3');
        audio.volume = 0.15;
        audio.loop = true;
        
        audio.play().catch(error => {
          console.log("Automaattinen musiikki estetty selaimessa:", error);
        });
        
        window.backgroundMusic = audio;
      }
    } else {
      // Pys√§yt√§ musiikki jos asetus on pois p√§√§lt√§
      if (window.backgroundMusic) {
        window.backgroundMusic.pause();
        window.backgroundMusic.currentTime = 0;
        window.backgroundMusic = null;
      }
    }
  }, [playMusic]); // Lis√§tty playMusic riippuvuudeksi

  // Kuuntele olemassa olevia huoneita joissa k√§ytt√§j√§ on mukana - korjattu versio
  useEffect(() => {
    if (!user?.uid || !isSearching) return;

    const q = query(
      collection(db, 'rooms'),
      where('userIds', 'array-contains', user.uid)
    );

    const unsubscribe = onSnapshot(q, (snapshot) => {
      // K√§sittele vain uusimmat muutokset t√§ss√§ sessiossa
      const changes = snapshot.docChanges().filter(change => change.type === 'added');
      
      changes.forEach(change => {
        const roomData = { id: change.doc.id, ...change.doc.data() };
        
        // Varmista ett√§ roomData on validia
        if (!roomData || !roomData.users || !Array.isArray(roomData.users)) {
          console.warn("‚ö†Ô∏è Virheellinen huonedata, ohitetaan:", roomData);
          return;
        }
        
        // Tarkista ett√§ huone on √§sken luotu (alle 30 sekuntia sitten)
        const roomAge = Date.now() - (roomData.createdAt?.toDate?.()?.getTime() || 0);
        const isNewRoom = roomAge < 30 * 1000; // 30 sekuntia
        
        if (roomData.isActive && isNewRoom) {
          console.log("üÜï L√∂ytyi uusi huone jossa olen mukana:", roomData.id);
          setIsSearching(false);
          setStatus('matched');
          onRoomJoined(change.doc.id, roomData);
        } else {
          console.log("‚è∞ Huone liian vanha tai ep√§aktiivinen, ohitetaan:", {
            age: Math.round(roomAge / 1000),
            isActive: roomData.isActive
          });
        }
      });
    }, (error) => {
      console.error("‚ùå Virhe huoneiden kuuntelussa:", error);
    });

    return unsubscribe;
  }, [user?.uid, isSearching, onRoomJoined]);

  // Luo chat-huone toisen k√§ytt√§j√§n kanssa
  const createChatRoom = async (otherUser) => {
    try {
      console.log("üèóÔ∏è Luodaan huone toisen k√§ytt√§j√§n kanssa:", otherUser);
      
      // Validoi otherUser
      if (!otherUser || !otherUser.id || !otherUser.name) {
        console.error("‚ùå Virheellinen otherUser:", otherUser);
        setStatus('error');
        return;
      }
      
      // Korjaa oma profiili jos ageGroup puuttuu (sama logiikka kuin startSearching:ss√§)
      let workingProfile = { ...profile };
      if (!workingProfile.ageGroup) {
        console.log("üîß Korjataan profiili createChatRoom:ssa - lis√§t√§√§n ageGroup");
        const calculateAgeGroup = (age) => {
          if (age >= 15 && age <= 17) return '15-17';
          return '18+';
        };
        const ageToUse = user.age || profile.age || 18;
        workingProfile.ageGroup = calculateAgeGroup(ageToUse);
      }
      
      // Validoi oma profiili
      if (!workingProfile?.displayName || !workingProfile?.ageGroup) {
        console.error("‚ùå Oma profiili puutteellinen:", workingProfile);
        setStatus('error');
        return;
      }
      
      // üîí EST√Ñ TUPLIEN LUONTI: Tarkista onko jo olemassa huone n√§iden k√§ytt√§jien v√§lill√§
      const existingRoomsQuery = query(
        collection(db, 'rooms'),
        where('userIds', 'array-contains', user.uid)
      );
      const existingSnapshot = await getDocs(existingRoomsQuery);
      
      const hasExistingRoom = existingSnapshot.docs.some(roomDoc => {
        const data = roomDoc.data();
        const userIds = data.userIds || [];
        return userIds.includes(otherUser.id) && data.isActive;
      });
      
      if (hasExistingRoom) {
        console.log("‚ö†Ô∏è Huone n√§iden k√§ytt√§jien v√§lill√§ on jo olemassa, ei luoda uutta");
        setStatus('idle');
        setIsSearching(false);
        return;
      }
      
      setStatus('matched');
      
      // Luo uniikki huone-ID
      const roomId = uuidv4();
      
      // Luo huone Firestoreen
      const roomData = {
        id: roomId,
        userIds: [user.uid, otherUser.id], // K√§yt√§ otherUser.id eik√§ .uid
        users: [
          {
            uid: user.uid,
            displayName: workingProfile.displayName,
            joinedAt: Date.now(),
            ready: false // Aluksi ei valmis
          },
          {
            uid: otherUser.id, // K√§yt√§ otherUser.id eik√§ .uid
            displayName: otherUser.name,
            joinedAt: Date.now(),
            ready: false // Aluksi ei valmis
          }
        ],
        ageGroup: workingProfile.ageGroup,
        createdAt: serverTimestamp(),
        isActive: true,
        bothReady: false, // Molemmat eiv√§t viel√§ valmiita
        type: 'text' // vain tekstichat, ei videota
      };

      console.log("üíæ Tallennettava roomData:", roomData);

      const docRef = await addDoc(collection(db, 'rooms'), roomData);
      
      // K√§yt√§ todellista document ID:t√§
      const actualRoomId = docRef.id;
      const actualRoomData = { ...roomData, id: actualRoomId };
      
      console.log("‚úÖ Huone luotu ID:ll√§:", actualRoomId);
      
      // Merkitse itsemme valmiiksi huoneessa - p√§ivit√§ koko users array s√§ilytt√§en sen arrayna
      const userIndex = roomData.users.findIndex(u => u.uid === user.uid);
      const updatedUsers = [...roomData.users];
      updatedUsers[userIndex] = { ...updatedUsers[userIndex], ready: true };
      
      await updateDoc(doc(db, 'rooms', actualRoomId), {
        users: updatedUsers
      });
      
      // Poista molemmat k√§ytt√§j√§t waiting-listasta - lis√§√§ viive est√§m√§√§n race condition
      await Promise.all([
        deleteDoc(doc(db, 'waiting', user.uid)).catch(e => console.warn("Oman waiting-poisto ep√§onnistui:", e)),
        deleteDoc(doc(db, 'waiting', otherUser.id)).catch(e => console.warn("Toisen waiting-poisto ep√§onnistui:", e))
      ]);
      
      // Pieni viive ennen siirtymist√§ chatiin - antaa aikaa Firestorelle synkronoida
      await new Promise(resolve => setTimeout(resolve, 500));
      
      console.log("üéâ Siirtym√§ chat-huoneeseen");
      
      // Siirry chat-huoneeseen (odottamaan toista)
      onRoomJoined(actualRoomId, actualRoomData);
      
    } catch (error) {
      console.error('Virhe huoneen luonnissa:', error);
      setStatus('idle');
      setIsSearching(false);
    }
  };

  // Aloita k√§ytt√§jien etsint√§
  const startSearching = async () => {
    if (isSearching) return;
    
    try {
      // üßπ SIIVOA ENSIN: Poista kaikki vanhat j√§√§nteet t√§lt√§ k√§ytt√§j√§lt√§
      try {
        // Poista mahdollinen vanha waiting-merkint√§
        await deleteDoc(doc(db, 'waiting', user.uid)).catch(e => console.log("Ei vanhaa waiting-merkint√§√§"));
        
        // Etsi ja poista kaikki vanhat ep√§aktiiviset huoneet joissa t√§m√§ k√§ytt√§j√§ on mukana
        const oldRoomsQuery = query(
          collection(db, 'rooms'),
          where('userIds', 'array-contains', user.uid)
        );
        const oldRoomsSnapshot = await getDocs(oldRoomsQuery);
        
        const cleanupPromises = [];
        oldRoomsSnapshot.docs.forEach(roomDoc => {
          const data = roomDoc.data();
          const roomAge = Date.now() - (data.createdAt?.toDate?.()?.getTime() || 0);
          
          // Poista vanhat (yli 3 min) tai ep√§aktiiviset huoneet - v√§hemm√§n aggressiivinen
          if (roomAge > 180000 || data.isActive === false) {
            console.log("üßπ Siivotaan vanha huone k√§ytt√§j√§lt√§:", roomDoc.id);
            cleanupPromises.push(deleteDoc(doc(db, 'rooms', roomDoc.id)));
          }
        });
        
        if (cleanupPromises.length > 0) {
          await Promise.all(cleanupPromises);
          console.log(`üßπ Siivottiin ${cleanupPromises.length} vanhaa huonetta k√§ytt√§j√§lt√§`);
        }
      } catch (cleanupError) {
        console.warn("‚ö†Ô∏è Siivous ep√§onnistui osittain:", cleanupError);
      }
      
      // Tarkista onko k√§ytt√§j√§ bannattu tai temp-bannattu
      const profileRef = doc(db, 'profiles', user.uid);
      const profileSnap = await getDoc(profileRef);
      
      if (profileSnap.exists()) {
        const profileData = profileSnap.data();
        
        // Tarkista ikuinen b√§nni
        if (profileData.banned) {
          alert('Et voi k√§ytt√§√§ palvelua. Syy: ' + (profileData.bannedReason || 'K√§ytt√∂ehtojen rikkominen'));
          return;
        }
        
        // Tarkista v√§liaikainen b√§nni
        if (profileData.temporaryBan?.active) {
          const bannedUntil = profileData.temporaryBan.bannedUntil.toDate ? 
            profileData.temporaryBan.bannedUntil.toDate() : 
            new Date(profileData.temporaryBan.bannedUntil);
          
          if (new Date() < bannedUntil) {
            // Temp-b√§nni on viel√§ voimassa
            const timeLeft = Math.ceil((bannedUntil - new Date()) / (1000 * 60 * 60)); // tunnit
            alert(`Et voi k√§ytt√§√§ palvelua viel√§ ${timeLeft} tuntia. Syy: ${profileData.temporaryBan.reason}`);
            return;
          } else {
            // Temp-b√§nni on vanhentunut, poista se
            console.log("üîì V√§liaikainen b√§nni vanhentunut, poistetaan");
            await updateDoc(profileRef, {
              'temporaryBan.active': false
            });
          }
        }
      }
      
      // Korjaa profiili jos ageGroup puuttuu
      let workingProfile = { ...profile };
      if (!workingProfile.ageGroup) {
        console.log("Korjataan profiili - lis√§t√§√§n ageGroup");
        
        // Laske ik√§ryhm√§ k√§ytt√§j√§n i√§n perusteella
        const calculateAgeGroup = (age) => {
          if (age >= 15 && age <= 17) return '15-17';
          return '18+'; // Kaikki 18+ samaan ryhm√§√§n
        };
        
        const ageToUse = user.age || profile.age || 18; // Fallback 18
        workingProfile.ageGroup = calculateAgeGroup(ageToUse);
        
        // P√§ivit√§ Firestore taustalla
        try {
          await setDoc(doc(db, 'profiles', user.uid), workingProfile);
        } catch (error) {
          console.warn("Firestore p√§ivitys ep√§onnistui:", error);
        }
      }

      // Varmista ett√§ profile on validi
      if (!workingProfile?.ageGroup || !workingProfile?.displayName) {
        console.error('Profile edelleen puutteellinen:', {
          profile: workingProfile,
          ageGroup: workingProfile?.ageGroup,
          displayName: workingProfile?.displayName,
          keys: Object.keys(workingProfile || {})
        });
        setStatus('Virhe: Profiili puutteellinen');
        return;
      }

      setIsSearching(true);
      setStatus('searching');
      setSearchStartTime(Date.now());
      
      // Tarkista onko jo odottavia k√§ytt√§ji√§ (ik√§pohjainen valinta)
      if (waitingUsers.length > 0) {
        // Sama logiikka kuin onSnapshotissa: sama ik√§ryhm√§ ensin, sitten nuorimmat
        const sameAgeGroup = waitingUsers.filter(u => u.ageGroup === workingProfile.ageGroup);
        let selectedUser;
        
        if (sameAgeGroup.length > 0) {
          sameAgeGroup.sort((a, b) => (a.age || 18) - (b.age || 18));
          selectedUser = sameAgeGroup[0];
        } else {
          waitingUsers.sort((a, b) => (a.age || 18) - (b.age || 18));
          selectedUser = waitingUsers[0];
        }
        
        await createChatRoom(selectedUser);
        return;
      }
      
      // Lis√§√§ itsens√§ waiting-listaan k√§ytt√§en user.uid:t√§ ID:n√§
      const waitingRef = doc(db, 'waiting', user.uid);
      await setDoc(waitingRef, {
        id: user.uid, // K√§yt√§ 'id' kentt√§√§ yhteensopivuuden vuoksi
        uid: user.uid, // Pid√§ uid my√∂s
        name: workingProfile.displayName,
        ageGroup: workingProfile.ageGroup,
        age: user.age || profile.age, // Lis√§√§ ik√§ matchmaking varten
        timestamp: Date.now()
      });

      console.log("Lis√§tty waiting listaan:", user.uid, "nimi:", workingProfile.displayName, "ageGroup:", workingProfile.ageGroup);
      
      // üöÄ OPTIMOITU QUERY: Kuuntele ensisijaisesti omaa ik√§ryhm√§√§
      const primaryQuery = query(
        collection(db, 'waiting'),
        where('ageGroup', '==', workingProfile.ageGroup)
      );
      
      // Fallback: jos ei l√∂ydy samasta ik√§ryhm√§st√§, kuuntele kaikkia
      let fallbackTimeout;
      
      console.log("Aloitetaan kuuntelu omaa ik√§ryhm√§√§:", workingProfile.ageGroup);
      
      // üìä PERFORMANCE TRACKING: Aloita matching mittaus
      const matchingStartTime = performance.now();
      const matchingMetrics = {
        startTime: matchingStartTime,
        ageGroup: workingProfile.ageGroup,
        waitingUsersChecked: 0,
        queryExecutions: 0
      };
      
      const unsubscribe = onSnapshot(primaryQuery, async (snapshot) => {
        matchingMetrics.queryExecutions++;
        matchingMetrics.waitingUsersChecked = snapshot.size;
        
        console.log(`onSnapshot (ageGroup ${workingProfile.ageGroup}), l√∂ytyi dokumentteja:`, snapshot.size);
        
        if (!snapshot.empty && isSearching) {
          // üîí RACE CONDITION PROTECTION: Tarkista ett√§ ei ole jo aktiivista huonetta
          const preCheckQuery = query(
            collection(db, 'rooms'),
            where('userIds', 'array-contains', user.uid),
            where('isActive', '==', true)
          );
          
          const preCheckSnapshot = await getDocs(preCheckQuery);
          if (!preCheckSnapshot.empty) {
            console.log("‚ö†Ô∏è Pre-check: K√§ytt√§j√§ll√§ on jo aktiivinen huone, lopetetaan haku");
            unsubscribe();
            return;
          }
          
          const allWaitingUsers = snapshot.docs
            .map(doc => ({
              id: doc.id,
              ...doc.data()
            }))
            .filter(waitingUser => waitingUser.uid !== user.uid); // Suodata oma uid pois
          
          console.log("Waiting k√§ytt√§j√§t (ilman omaa):", allWaitingUsers);
          
          if (allWaitingUsers.length > 0) {
            // üéØ OPTIMOITU: Kaikki l√∂ydetyt ovat samasta ik√§ryhm√§st√§, valitse nuorin
            allWaitingUsers.sort((a, b) => (a.age || 18) - (b.age || 18));
            const selectedUser = allWaitingUsers[0];
            console.log("üéØ Valittu nuorin samasta ik√§ryhm√§st√§:", selectedUser.age, "vuotta");
            
            console.log("L√∂ytyi match (sama ik√§ryhm√§):", selectedUser);
            
            // üìä PERFORMANCE METRICS: Kirjaa matching-aika
            const matchingEndTime = performance.now();
            const matchingDuration = matchingEndTime - matchingMetrics.startTime;
            
            console.log(`üèÜ MATCHING METRICS:`, {
              duration: `${matchingDuration.toFixed(2)}ms`,
              ageGroup: matchingMetrics.ageGroup,
              waitingUsersChecked: matchingMetrics.waitingUsersChecked,
              queryExecutions: matchingMetrics.queryExecutions,
              matchType: 'primary-agegroup'
            });
            
            // üîí TARKISTA VIEL√Ñ KERRAN ett√§ meill√§ ei ole jo huonetta t√§m√§n k√§ytt√§j√§n kanssa
            const doubleCheckQuery = query(
              collection(db, 'rooms'),
              where('userIds', 'array-contains', user.uid)
            );
            
            try {
              const doubleCheckSnapshot = await getDocs(doubleCheckQuery);
              const hasActiveRoom = doubleCheckSnapshot.docs.some(roomDoc => {
                const data = roomDoc.data();
                const userIds = data.userIds || [];
                return userIds.includes(selectedUser.id) && data.isActive;
              });
              
              if (hasActiveRoom) {
                console.log("‚ö†Ô∏è Double-check: Huone on jo olemassa, ohitetaan match");
                return;
              }
              
              // ‚úÖ Kaikki kunnossa, luodaan huone
              setStatus('L√∂ytyi match! Luodaan chat...');
              unsubscribe(); // Lopeta kuuntelu
              
              await createChatRoom(selectedUser);
              
            } catch (doubleCheckError) {
              console.error("‚ùå Virhe double-check:ss√§:", doubleCheckError);
              // Jatka normaalisti jos tarkistus ep√§onnistuu
              setStatus('L√∂ytyi match! Luodaan chat...');
              unsubscribe(); // Lopeta kuuntelu
              await createChatRoom(selectedUser);
            }
          }
        } else if (isSearching) {
          // üîÑ FALLBACK: Jos omasta ik√§ryhm√§st√§ ei l√∂ydy ket√§√§n, kokeile 10s kuluttua kaikkia
          if (!fallbackTimeout) {
            console.log("‚è≥ Ei l√∂ytynyt omasta ik√§ryhm√§st√§, aloitetaan fallback 10s kuluttua...");
            window.matchmakerFallbackTimeout = setTimeout(async () => {
              if (!isSearching) return; // Lopetettu jo
              
              console.log("üîÑ Fallback: kuunnellaan kaikkia ik√§ryhmi√§...");
              
              // Vaihda kuuntelemaan kaikkia k√§ytt√§ji√§  
              unsubscribe(); // Lopeta nykyinen listener
              
              const fallbackQuery = query(collection(db, 'waiting'));
              const fallbackUnsubscribe = onSnapshot(fallbackQuery, async (fallbackSnapshot) => {
                if (!fallbackSnapshot.empty && isSearching) {
                  const allUsers = fallbackSnapshot.docs
                    .map(doc => ({ id: doc.id, ...doc.data() }))
                    .filter(u => u.uid !== user.uid);
                    
                  if (allUsers.length > 0) {
                    // Valitse nuorin kaikista
                    allUsers.sort((a, b) => (a.age || 18) - (b.age || 18));
                    const selectedUser = allUsers[0];
                    console.log("üîÑ Fallback match:", selectedUser);
                    
                    // üìä FALLBACK METRICS
                    const fallbackEndTime = performance.now();
                    const totalDuration = fallbackEndTime - matchingMetrics.startTime;
                    
                    console.log(`üîÑ FALLBACK METRICS:`, {
                      totalDuration: `${totalDuration.toFixed(2)}ms`,
                      fallbackDelay: '10000ms',
                      matchType: 'fallback-all-ages'
                    });
                    
                    setStatus('L√∂ytyi match! Luodaan chat...');
                    fallbackUnsubscribe();
                    await createChatRoom(selectedUser);
                  }
                }
              });
              
              setUnsubscribe(() => fallbackUnsubscribe);
            }, 10000); // 10 sekunnin fallback
          }
        }
      }, (error) => {
        console.error("Virhe waiting lista kuuntelussa:", error);
        setIsSearching(false);
        setStatus('idle');
      });
      
      // Tallenna unsubscribe funktio my√∂hemp√§√§ k√§ytt√∂√§ varten
      setUnsubscribe(() => unsubscribe);
      
    } catch (error) {
      console.error('Virhe etsinn√§n aloituksessa:', error);
      setIsSearching(false);
      setStatus('idle');
    }
  };

  // Lopeta etsint√§ ja siivoa kaikki resurssit
  const stopSearching = async () => {
    try {
      setIsSearching(false);
      setStatus('idle');
      setSearchStartTime(null);
      
      // Lopeta listener jos on k√§ynniss√§
      if (unsubscribe) {
        unsubscribe();
        setUnsubscribe(null);
      }
      
      // üîß MEMORY LEAK FIX: Siivoa fallback timeout
      if (window.matchmakerFallbackTimeout) {
        clearTimeout(window.matchmakerFallbackTimeout);
        window.matchmakerFallbackTimeout = null;
      }
      
      // Poista itsemme waiting-listasta
      await deleteDoc(doc(db, 'waiting', user.uid));
      
    } catch (error) {
      console.error('Virhe etsinn√§n lopetuksessa:', error);
    }
  };

  // Piilotus-toiminto poistettu k√§ytt√§j√§n pyynn√∂st√§

  // Laske etsint√§aika
  const getSearchDuration = () => {
    if (!searchStartTime) return 0;
    return Math.floor((Date.now() - searchStartTime) / 1000);
  };

  return (
    <div className="matchmaker-container">
      <div className="matchmaker-content">
        <h2>üîç Etsi chattikaveria</h2>
        
        {/* Aktiivisten k√§ytt√§jien n√§ytt√∂ */}
        <div className="user-stats">
          <p>üë• K√§ytt√§j√§t sivustolla: <strong>{activeUsersCount}</strong></p>
          <button 
            onClick={() => setShowFeedbackModal(true)} 
            className="feedback-link-btn"
          >
            üí¨ Anna palautetta
          </button>
        </div>
        
        {status === 'idle' && (
          <div className="search-controls">
            <button 
              onClick={startSearching}
              className="start-search-btn"
            >
              üöÄ ALOITA HAKU
            </button>
            
            <div className="info-box">
              <p>üí° <strong>N√§in se toimii:</strong></p>
              <ul>
                <li>üéØ Etsimme sinulle samanik√§ist√§ henkil√∂√§</li>
                <li>üí¨ Kun l√∂yd√§mme, chat alkaa automaattisesti</li>
                <li>‚ö° Prosessi kest√§√§ yleens√§ alle minuutin</li>
                <li>üîÑ Voit aloittaa uuden haun milloin tahansa</li>
              </ul>
            </div>
          </div>
        )}

        {status === 'searching' && (
          <div className="searching-status">
            <div className="spinner">üîÑ</div>
            <h3>Etsit√§√§n chattikaveria...</h3>
            <p>‚è±Ô∏è Aikaa kulunut: {getSearchDuration()} sekuntia</p>
            <p>üë• Aktiivisia k√§ytt√§ji√§ sivustolla: {activeUsersCount}</p>
            
            <button 
              onClick={stopSearching}
              className="stop-search-btn"
            >
              ‚èπÔ∏è Lopeta haku
            </button>
            
            <div className="tips">
              <p>üí° <strong>Vinkki:</strong> Jos haku kest√§√§ kauan, kokeile hetken p√§√§st√§ uudelleen!</p>
            </div>
          </div>
        )}

        {status === 'matched' && (
          <div className="matched-status">
            <div className="success-icon">üéâ</div>
            <h3>L√∂ytyi chattikaveri!</h3>
            <p>Siirryt√§√§n chat-huoneeseen...</p>
          </div>
        )}
      </div>

      {/* Feedback Modal */}
      <FeedbackModal 
        isOpen={showFeedbackModal} 
        onClose={() => setShowFeedbackModal(false)} 
      />
    </div>
  );
};

